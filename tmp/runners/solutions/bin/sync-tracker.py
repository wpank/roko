#!/usr/bin/env python3
"""
sync-tracker.py — flip ISSUE-TRACKER.md checkboxes from the git log.

Each batch lands with a commit-message trailer:

    tracker: <BATCH_ID> done [<short-sha-or-comment>]

This script scans git log on the current branch for those trailers and
flips the matching `[ ]` to `[x]` in ISSUE-TRACKER.md.

Usage:
  python3 bin/sync-tracker.py             # report drift, no writes
  python3 bin/sync-tracker.py --apply     # write changes
  python3 bin/sync-tracker.py --since 2026-04-29  # restrict git scan
"""

from __future__ import annotations

import argparse
import re
import subprocess
import sys
from pathlib import Path

RUNNER_DIR = Path(__file__).resolve().parents[1]
TRACKER_MD = RUNNER_DIR / "ISSUE-TRACKER.md"
STATUS_MD = RUNNER_DIR / "STATUS.md"

TRAILER_RE = re.compile(
    r"^\s*tracker:\s+([A-Z][A-Z0-9_]+_\d+)\s+done(?:\s+(.+))?$",
    re.MULTILINE,
)


def collect_done_ids(since: str | None) -> dict[str, str]:
    cmd = ["git", "log", "--format=%H%x00%B%x1e"]
    if since:
        cmd.append(f"--since={since}")
    out = subprocess.run(cmd, check=True, capture_output=True, text=True, cwd=RUNNER_DIR.parents[2]).stdout
    done: dict[str, str] = {}
    for entry in out.split("\x1e"):
        entry = entry.strip()
        if not entry:
            continue
        sha, _, msg = entry.partition("\x00")
        for m in TRAILER_RE.finditer(msg):
            bid = m.group(1)
            note = (m.group(2) or sha[:8]).strip()
            done[bid] = note
    return done


def flip_tracker(text: str, done: dict[str, str]) -> tuple[str, list[str], list[str]]:
    flipped: list[str] = []
    not_found: list[str] = list(done.keys())
    out_lines: list[str] = []
    for line in text.splitlines():
        m = re.match(
            r'^(\| <a id="([a-z0-9-]+)"></a> )\[ \] (`([A-Z][A-Z0-9_]+_\d+)`)(\s*\|.*)$',
            line,
        )
        if m:
            bid = m.group(3)
            if bid in done:
                line = f"{m.group(1)}[x] `{bid}`  <!-- {done[bid]} -->{m.group(4)}"
                flipped.append(bid)
                if bid in not_found:
                    not_found.remove(bid)
        out_lines.append(line)
    return "\n".join(out_lines) + ("\n" if not text.endswith("\n") else ""), flipped, not_found


def update_status(tracker_text: str) -> str:
    """Recompute the per-prefix Open/Total counts in STATUS.md."""
    row_cell = re.compile(
        r'<a id="[^"]+"></a> (\[\s*\]|\[x\]|\[~\]) `([A-Z][A-Z0-9_]+_\d+)`',
    )
    counts_open: dict[str, int] = {}
    counts_total: dict[str, int] = {}
    current_prefix: str | None = None
    for line in tracker_text.splitlines():
        m = re.match(r"^## (\w+) — ", line)
        if m:
            current_prefix = m.group(1)
            counts_open.setdefault(current_prefix, 0)
            counts_total.setdefault(current_prefix, 0)
            continue
        if current_prefix:
            mm = row_cell.search(line)
            if mm:
                counts_total[current_prefix] += 1
                if mm.group(1) != "[x]":
                    counts_open[current_prefix] += 1
    lines = [
        "# Status Snapshot",
        "",
        "> Generated by `bin/sync-tracker.py`. Reflects ISSUE-TRACKER.md.",
        "",
        "| Prefix | Open | Total | % done |",
        "|---|---:|---:|---:|",
    ]
    grand_open = 0
    grand_total = 0
    for prefix in sorted(counts_total):
        open_n = counts_open[prefix]
        total = counts_total[prefix]
        pct = 0.0 if total == 0 else 100.0 * (total - open_n) / total
        grand_open += open_n
        grand_total += total
        lines.append(f"| `{prefix}` | {open_n} | {total} | {pct:.0f}% |")
    overall_pct = 0.0 if grand_total == 0 else 100.0 * (grand_total - grand_open) / grand_total
    lines.append(f"| **Total** | **{grand_open}** | **{grand_total}** | **{overall_pct:.0f}%** |")
    return "\n".join(lines) + "\n"


def main(argv: list[str]) -> int:
    p = argparse.ArgumentParser()
    p.add_argument("--apply", action="store_true", help="write changes")
    p.add_argument("--since", default=None, help="git --since= filter")
    args = p.parse_args(argv)

    if not TRACKER_MD.exists():
        print(f"[error] {TRACKER_MD} not found; run generate-prompts.py first", file=sys.stderr)
        return 1

    done = collect_done_ids(args.since)
    if not done:
        print("[info] no tracker trailers found in git log")
        return 0

    text = TRACKER_MD.read_text()
    new_text, flipped, not_found = flip_tracker(text, done)

    print(f"[info] {len(flipped)} batches flipped to [x]")
    if not_found:
        print(f"[warn] {len(not_found)} done-trailers had no matching tracker row:")
        for bid in not_found:
            print(f"   - {bid}")

    if args.apply:
        TRACKER_MD.write_text(new_text)
        STATUS_MD.write_text(update_status(new_text))
        print(f"[done] wrote {TRACKER_MD} and {STATUS_MD}")
    else:
        print("[info] dry-run; pass --apply to write")

    return 0


if __name__ == "__main__":
    sys.exit(main(sys.argv[1:]))
